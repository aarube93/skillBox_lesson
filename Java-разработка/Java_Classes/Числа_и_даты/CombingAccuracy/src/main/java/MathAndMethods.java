public class MathAndMethods {
    public static void main(String[] args) {
        System.out.println(Math.abs(-987675));//=987675, вычесляет значение по модулю
        System.out.println(Math.max(898, 75));//Просто возвращает найбольшее число
        System.out.println(Math.min(898,76));//тоже, только минимальное
        System.out.println(Math.pow(5,3));//Возводит число в степень. Число 5 возведет в степень 3
        System.out.println(Math.sqrt(625));//Возвращает квадратный корень от числав скобках
        System.out.println(Math.cbrt(8));//Возвращает кубический корень числа
        System.out.println(Math.round(6.49));//Округляет по всем математическим правилам
        System.out.println(Math.random());//Просто генерит случайное число от 0 до 1 невключительно
    }
}